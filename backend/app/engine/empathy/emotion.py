def analyze_user_tone(input_text: str) -> str | None:
    """分析用户输入的语气/情绪状态，返回中文描述或 None"""
    trimmed = input_text.strip()
    if not trimmed:
        return None

    for keyword, description in TONE_PATTERNS:
        if trimmed == keyword or trimmed.startswith(keyword + "…") or trimmed.startswith(keyword + "..."):
            return description

    if len(trimmed) <= 2:
        return "用户仅给出了极简回应，动力较低，需要温和关注。"
    if len(trimmed) <= 4:
        return "用户给出了非常简短的回答，可能需要进一步鼓励展开。"

    return None


class EmotionAnalyzer:
    """情绪分析器 — 基于用户输入的语调和内容调整策略"""

    TONE_STRATEGY_MAP = {
        "拒绝": "DEFLECT",       # 不追问，转移话题到安全区域
        "对抗": "DEFLECT",
        "回避": "GENTLE_PROBE",  # 温和再探
        "犹豫": "ENCOURAGE",     # 鼓励展开
        "不确定": "ENCOURAGE",
        "模糊": "CLARIFY",      # 帮助澄清
        "消极": "RAPPORT_FIRST", # 先共情建立信任
        "淡漠": "RAPPORT_FIRST",
        "焦虑": "SOOTHE",       # 安抚优先
        "无助": "SOOTHE",
        "耗竭": "SOOTHE",       # 心理耗竭
        "烦躁": "SOOTHE",       # 高度烦躁
        "抑郁": "SOOTHE",       # 抑郁相关
        "压力": "SOOTHE",       # 压力过大
        "结束": "RESPECT_EXIT", # 尊重用户意愿
    }

    @staticmethod
    def get_strategy(tone_analysis: str | None) -> str:
        if tone_analysis is None:
            return "NORMAL"
        for keyword, strategy in EmotionAnalyzer.TONE_STRATEGY_MAP.items():
            if keyword in tone_analysis:
                return strategy
        return "NORMAL"
